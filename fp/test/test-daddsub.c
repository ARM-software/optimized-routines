double
make_double (uint64_t x)
{
  double r;
  memcpy (&r, &x, sizeof (r));
  return r;
}

uint64_t
unmake_double (double x)
{
  uint64_t r;
  memcpy (&r, &x, sizeof (r));
  return r;
}

int
main (void)
{
  bool failed = false;

  for (size_t i = 0; i < sizeof (tests) / sizeof (tests[0]); i++)
    {
      const struct test *t = &tests[i];

#ifdef USE_NATIVE_ARITHMETIC
      /* If you compile with USE_NATIVE_ARITHMETIC defined, the same
       * set of tests will be run using the toolchain's built in double
       * arithmetic, instead of calling arm_fp_dmul. */
      double in1 = make_double (t->in1);
      double in2 = make_double (t->in2);
      double out = (t->op == ADD ? in1 + in2 : in1 - in2);
      uint64_t outbits = unmake_double (out);
#else
      extern uint64_t arm_fp_dadd(uint64_t, uint64_t);
      extern uint64_t arm_fp_dsub(uint64_t, uint64_t);
      uint64_t outbits = (t->op == ADD ? arm_fp_dadd(t->in1, t->in2) :
                          arm_fp_dsub(t->in1, t->in2));
#endif

      if (outbits != t->out)
	{
	  printf ("FAIL: %s(%016" PRIx64 ", %016" PRIx64 ") -> %016" PRIx64
		  ", expected %016" PRIx64 "\n",
                  (t->op == ADD ? "dadd" : "dsub"),
		  t->in1, t->in2, outbits, t->out);
	  failed = true;
	}
    }

  if (!failed)
    printf ("all passed\n");

  return failed;
}
