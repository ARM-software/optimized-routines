float
make_float (uint32_t x)
{
  float r;
  memcpy (&r, &x, sizeof (r));
  return r;
}

uint32_t
unmake_float (float x)
{
  uint32_t r;
  memcpy (&r, &x, sizeof (r));
  return r;
}

int
main (void)
{
  bool failed = false;

  for (size_t i = 0; i < sizeof (tests) / sizeof (tests[0]); i++)
    {
      const struct test *t = &tests[i];

#ifdef USE_NATIVE_ARITHMETIC
      /* If you compile with USE_NATIVE_ARITHMETIC defined, the same
       * set of tests will be run using the toolchain's built in float
       * arithmetic, instead of calling arm_fp_fdiv. */
      float in1 = make_float (t->in1);
      float in2 = make_float (t->in2);
      float out = in1 / in2;
      uint32_t outbits = unmake_float (out);
#else
      extern uint32_t arm_fp_fdiv(uint32_t, uint32_t);
      uint32_t outbits = arm_fp_fdiv(t->in1, t->in2);
#endif

      if (outbits != t->out)
	{
	  printf ("FAIL: fdiv(%08" PRIx32 ", %08" PRIx32 ") -> %08" PRIx32
		  ", expected %08" PRIx32 "\n",
		  t->in1, t->in2, outbits, t->out);
	  failed = true;
	}
    }

  if (!failed)
    printf ("all passed\n");

  return failed;
}
